package Loop;

public class $2_DoWhile {

    public static void main(String[] args) {
        int count = 0;
        do {
            System.out.println("Hello World"); //Se thuc hien dong nay truoc 1 lan
            count++;
        }while (count < 0); //Sau do kiem tra dieu kien de chay tiep hay dung lai
    }
}
